print("python has three numeric types: int, float and complex")

# A variable is like a labeled box that stores information. Y
# ou can change the contents of the box, but the label stays the same. 

#int datatype
myValue=2
print(myValue)
print(type(myValue))
print(str(myValue) + " is for the data type " + str(type(myValue)))

#float datatype
myValue=1.24
print(myValue)
print(type(myValue))
print(str(myValue) + " is for the datatype" + str(type(myValue)))

#complex data type
myValue=2j
print(myValue)
print(type(myValue))
print(str(myValue) + " is for the datatype" + str(type(myValue)))

